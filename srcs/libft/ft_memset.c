#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	void	*d;

	d = b;
	i = 0;
	while (i < len)
	{
		*(unsigned char *)(d + i) = (unsigned char)c;
		i++;
	}
	return (d);
}
